    def stream_completion(self, prompt, temperature, max_tokens):
        self.send_response(200)
        self.send_header('Content-Type', 'text/event-stream')
        self.send_cors_headers()
        self.end_headers()
        
        generated_text = generate_response(prompt, temperature, max_tokens)
        tokens = generated_text.split()
        
        for i, token in enumerate(tokens):
            chunk_data = {
                'id': f'cmpl-{uuid.uuid4()}',
                'object': 'text_completion',
                'created': int(time.time()),
                'model': model_state.model_name or 'custom-model',
                'choices': [
                    {
                        'text': token + ' ',
                        'index': 0,
                        'logprobs': None,
                        'finish_reason': None
                    }
                ]
            }
            
            self.wfile.write(f"data: {json.dumps(chunk_data)}\n\n".encode('utf-8'))
            self.wfile.flush()
            time.sleep(0.05)
        
        final_chunk = {
            'id': f'cmpl-{uuid.uuid4()}',
            'object': 'text_completion',
            'created': int(time.time()),
            'model': model_state.model_name or 'custom-model',
            'choices': [
                {
                    'text': '',
                    'index': 0,
                    'logprobs': None,
                    'finish_reason': 'stop'
                }
            ]
        }
        
        self.wfile.write(f"data: {json.dumps(final_chunk)}\n\n".encode('utf-8'))
        self.wfile.write(b"data: [DONE]\n\n")
        self.wfile.flush()
    
    def serve_chat_completion(self, data):
        if not model_state.model_loaded:
            response_data = {
                'error': {
                    'message': 'No model loaded',
                    'type': 'invalid_request_error'
                }
            }
